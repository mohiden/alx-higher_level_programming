#!/usr/bin/python3

def add(a, b):
    """My addition function

    Args:
        a: int
        b: int

    Returns:
        the sum of a and b

    """
    return a + b


def sub(a, b):
    """My subtraction function

    Args:
        a: int
        b: int

    Returns:
        the subtraction of a and b

    """
    return a - b


def mul(a, b):
    """My multiplication function

    Args:
        a: int
        b: int

    Returns:
        the multiplication of a and b

    """
    return a * b


def div(a, b):
    """My division function

    Args:
        a: int
        b: int

    Returns:
        the division of a and b

    """
    return a / b
